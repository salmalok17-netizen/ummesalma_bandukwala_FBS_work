x = 10
y = 10.0
z = '10'

#1. == (equals to)
print(x == z)
print(x == y)


#2. !=  (not equals to)
print(x != z)


#3. >  (greater than)
print (x > 5)
print (y > 5)
#print (z > 5)   #int and str cannot be compared so will give error


#4. >=  (greater than equal to)
print(x >= 10)


#5. <  (less than)
print(x < 20)


#6. <=  (less than equal to)
print(x <= 10)

