#1. and - if both operands are True then it will return True, otherwise False
print(True and True)
print(True and False) 

#2. or - if both operands are False then it will return False, otherwise True
print(True or True)
print(False or False)


#3. not - It will give opposite of operand
print(not True)
print(not 10)


#Falsy values - 0,None,False,blank(all data structures)
print(not {})
print(not 0)