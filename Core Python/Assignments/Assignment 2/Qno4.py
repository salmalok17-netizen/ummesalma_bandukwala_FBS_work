base = float(input("Enter base of the Triangle:"))
height = float(input("Enter height of the Triangle:"))

l = float(input("Enter lenght of a Rectangle:"))
w = float(input("Enter width of a Rectangle:"))

area_of_tri = 1/2 * base * height
print(f"Area of Triangle is= {area_of_tri}")

area_of_rect = l * w
print(f"Area of Rectangle is= {area_of_rect}")